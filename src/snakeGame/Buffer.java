
package snakeGame;

public class Buffer {
	
	private String currentDirection = "";
	
	public Buffer() {
		
	}
	
	void goUp() {
		currentDirection = "up";
	}
	
	void goLeft() {
		currentDirection = "up";
	}
	
	void goDown() {
		currentDirection = "up";
	}
	
	void goRight() {
		currentDirection = "up";
	}
	
	String getcurrentDirection() {
		return currentDirection;
		
	}
	
}
